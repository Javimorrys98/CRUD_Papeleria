    //Para listar los productos
    private void btnListarListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarListarActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf= Persistence.createEntityManagerFactory("persistence");
        
        TablapapeleriaJpaController em=new TablapapeleriaJpaController(emf);
        
        List<Tablapapeleria> lista=em.findTablapapeleriaEntities();
        
        String nombres[] = {"ID", "Codigo", "Producto", "Familia", "Precio"};
        
        Object rowData[][] = new Object[lista.size()][5];
        int i = 0;

        for (Tablapapeleria a: lista){
            rowData[i][0] = a.getId();
            rowData[i][1] = a.getCodigo();
            rowData[i][2] = a.getProducto();
            rowData[i][3] = a.getFamilia();
            rowData[i][4] = a.getPrecio();
            i++;
        }
        DefaultTableModel tablem= new DefaultTableModel(rowData,nombres);

        tblListarProductos.setModel(tablem);
    }//GEN-LAST:event_btnListarListarActionPerformed

